def handle_correlated_features_strategic(df, continuous_vars, threshold=0.7):
    """
    Strategic removal of correlated features considering measurement scales
    """
    corr_matrix = df[continuous_vars].corr().abs()

    # Find highly correlated pairs
    high_corr_pairs = []
    for i in range(len(corr_matrix.columns)):
        for j in range(i+1, len(corr_matrix.columns)):
            if corr_matrix.iloc[i, j] > threshold:
                high_corr_pairs.append({
                    'feature1': corr_matrix.columns[i],
                    'feature2': corr_matrix.columns[j],
                    'correlation': corr_matrix.iloc[i, j]
                })

    # Strategic removal prioritizing standardized scales over Likert scales
    standardized_scales = ['anxiety_level', 'self_esteem', 'depression']
    features_to_remove = set()

    for pair in high_corr_pairs:
        feat1, feat2 = pair['feature1'], pair['feature2']

        # Strategy: Keep standardized scales, remove Likert scales when correlated
        if feat1 in standardized_scales and feat2 not in standardized_scales:
            features_to_remove.add(feat2)  # Remove Likert scale
            print(f"Keeping standardized scale '{feat1}', removing '{feat2}' (r={pair['correlation']:.3f})")

        elif feat2 in standardized_scales and feat1 not in standardized_scales:
            features_to_remove.add(feat1)  # Remove Likert scale
            print(f"Keeping standardized scale '{feat2}', removing '{feat1}' (r={pair['correlation']:.3f})")

        # If both are standardized, keep the one with higher variance
        elif feat1 in standardized_scales and feat2 in standardized_scales:
            var1, var2 = df[feat1].var(), df[feat2].var()
            if var1 >= var2:
                features_to_remove.add(feat2)
                print(f"Both standardized: keeping '{feat1}' (higher variance), removing '{feat2}'")
            else:
                features_to_remove.add(feat1)
                print(f"Both standardized: keeping '{feat2}' (higher variance), removing '{feat1}')")

        # If both are Likert scales, keep the more interpretable one
        else:
            # Prefer features with clearer domain meaning
            preferred_features = ['sleep_quality', 'academic_performance', 'social_support']
            if feat1 in preferred_features:
                features_to_remove.add(feat2)
                print(f"Both Likert: keeping preferred '{feat1}', removing '{feat2}'")
            elif feat2 in preferred_features:
                features_to_remove.add(feat1)
                print(f"Both Likert: keeping preferred '{feat2}', removing '{feat1}'")
            else:
                # Default: remove the one with lower variance
                var1, var2 = df[feat1].var(), df[feat2].var()
                if var1 >= var2:
                    features_to_remove.add(feat2)
                    print(f"Both Likert: keeping '{feat1}' (higher variance), removing '{feat2}'")
                else:
                    features_to_remove.add(feat1)
                    print(f"Both Likert: keeping '{feat2}' (higher variance), removing '{feat1}')")


    final_features = [f for f in continuous_vars if f not in features_to_remove]

    print(f"\nRemoved {len(features_to_remove)} features: {list(features_to_remove)}")
    print(f"Final continuous features: {len(final_features)}")

    return final_features, list(features_to_remove)

