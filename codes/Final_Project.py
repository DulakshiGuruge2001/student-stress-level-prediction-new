def select_optimal_components(eigenvalues, cumulative_variance, threshold=80):
    """
    Automatically suggest optimal number of components
    """
    # Kaiser criterion (eigenvalue > 1)
    kaiser_components = np.sum(eigenvalues > 1)

    # Cumulative variance threshold (default: 80%)
    variance_components = np.argmax(cumulative_variance >= threshold) + 1 if any(cumulative_variance >= threshold) else len(cumulative_variance)

    # Elbow method (simplified - point where gain drops significantly)
    # Find the largest drop after the first component
    diff = np.diff(eigenvalues)
    # Avoid dividing by zero or very small numbers for relative drop calculation
    relative_drop = np.abs(diff) / (np.abs(eigenvalues[:-1]) + 1e-8)
    # Find the index of the largest relative drop after the first component
    if len(relative_drop) > 1:
        elbow_idx_relative = np.argmax(relative_drop[1:]) + 2
    else:
        elbow_idx_relative = 1 # If only one drop, elbow is the first component

    # Also consider the largest absolute drop
    if len(diff) > 1:
         elbow_idx_absolute = np.argmax(np.abs(diff[1:])) + 2
    else:
         elbow_idx_absolute = 1 # If only one drop, elbow is the first component

    # Consider the minimum of these heuristic methods, but at least 2 components
    # A more robust elbow detection might be needed for complex cases
    suggested_elbow = min(elbow_idx_relative, elbow_idx_absolute)
    suggested_elbow = max(2, suggested_elbow) # Ensure at least 2 components


    print(f"\nOptimal Component Selection Heuristics:")
    print(f"• Kaiser criterion (eigenvalue > 1): {kaiser_components} components")
    print(f"• Cumulative variance ≥ {threshold}%: {variance_components} components")
    print(f"• Suggested elbow point (visual inspection recommended): {suggested_elbow} components")

    # Suggestion based on Kaiser or elbow, preferring Kaiser if it suggests at least 1
    optimal_n = max(1, kaiser_components) if kaiser_components >= 1 else suggested_elbow
    optimal_n = min(optimal_n, len(eigenvalues)) # Do not suggest more components than available
    optimal_n = max(1, optimal_n) # Ensure at least 1 component

    print(f"\nSuggested number of components for final analysis: {optimal_n}")

    return optimal_n

